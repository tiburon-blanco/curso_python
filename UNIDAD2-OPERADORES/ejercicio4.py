# Escribir un programa que pregunte la edad una familia TIPO y calcule su promedio
P=int(input("Ingrese la edad del Padre: "))
M=int(input("Ingrese la edad de la Madre: "))
H1=int(input("Ingrese la edad del Hije mayor: "))
H2=int(input("Ingrese la edad del Hije menor: "))

promedio=(P+M+H1+H2)/4
print("La edad promedio es:",promedio)

