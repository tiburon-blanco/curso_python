# REPETICONES o BUCLES o  LOOPS
# Son lineas de codigo se se repiten N catidad de veces

# Existe 2 tipos de repeticones:

# A) FOR     = Repetir Para.     De enstrada se sabe cuantas veces se va a repetir el loop
# B) WHILE   = Repetir mientras. Se evalua a traves de una condicion que tiene que ser modificada dentro del loop

# Se puede iterar o loopear: string, listas, diccionarios, tuplas, sets

# Se puede iterar también con RANGE
# range(start, stop, step)

# start: Es Opcional. Un número entero que especifica en qué posición comenzar. El valor predeterminado es 0
# stop: Es Obligatorio. Un número entero que especifica en qué posición detenerse.
# step: Es Opcional. Un número entero que especifica el incremento. El valor predeterminado es 1

# Ejemplos:

# range(10) = range(0, 10, 1) = itera 10 veces. 
# range(10, 100, 5) = itera del 10 al 100 y va de 5 en 5


# Se pude enumerar para saber el índice de la iteración con ENUMERATE
# En indice aveces es neceario para saber la posición de recorrido
# enumerate(items, start)
# items: pueder ser un range o una colección (string, listas, diccionarios, tuplas, sets)
# start: Es Opcional. Un número entero que especifica en qué posición comenzar. El valor predeterminado es 0